    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String n1= txtnum1.getText();
         String n2= txtnum2.getText();
         if ( opsuma.isSelected()){
             int a = Integer.parseInt(n1);
             int b =Integer.parseInt(n2);
             int c = a +b;
             Bresultado.setText(Integer.toString(c));
         }
        
         if ( opresta.isSelected()){
             int a = Integer.parseInt(n1);
             int b =Integer.parseInt(n2);
             int c = a -b;
             Bresultado.setText(Integer.toString(c));
         }
         if ( opmultiplicacion.isSelected()){
             int a = Integer.parseInt(n1);
             int b =Integer.parseInt(n2);
             int c = a *b;
             Bresultado.setText(Integer.toString(c));
         }
        if ( opdiv.isSelected()){
             float a = Float.parseFloat(n1);
             float b =Float.parseFloat(n2);
             float c = a /b;
             Bresultado.setText(Float.toString(c));
         }
        
    }//GEN-LAST:event_jButton5ActionPerformed


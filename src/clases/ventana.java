    private void txtnum1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnum1KeyTyped
        // TODO add your handling code here:
       int key = evt.getKeyChar();
    if (key==8) {
             c--;  
    }
    
          if (c<0){
              c=0;
          }
             
             if ( (key >= 48 && key<=57) ||  key== 46 ){
           c++;
       } else {
           evt.consume();
         if (c>0){
            btnsuma.setEnabled(true);
              btnresta.setEnabled(true);
               btnmulti.setEnabled(true);
                btndivision.setEnabled(true);
                opsuma.setEnabled(true);
                opresta.setEnabled(true);
                opmultiplicacion.setEnabled(true);
                opdiv.setEnabled(true);
         
         }else{
             btnsuma.setEnabled(false);
              btnresta.setEnabled(false);
               btnmulti.setEnabled(false);
                btndivision.setEnabled(false);
                 opsuma.setEnabled(false);
                opresta.setEnabled(false);
                opmultiplicacion.setEnabled(false);
                opdiv.setEnabled(false);
         
         }
       
       }
    }//GEN-LAST:event_txtnum1KeyTyped

